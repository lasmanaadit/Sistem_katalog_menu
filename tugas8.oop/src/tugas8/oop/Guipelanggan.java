    private void smpn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_smpn2ActionPerformed
        // TODO add your handling code here:
    String namaPelanggan = txt_nama.getText();
    String menuPelanggan = txt_menu.getText();
    String kategoriMenu = cmb_kategori.getSelectedItem().toString();

    // Coba parsing jumlah makanan dari text field baru
    int jumlah;
    try {
        jumlah = Integer.parseInt(txt_jumlah.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Harus memasukkan angka untuk jumlah!");
        return; // Stop eksekusi lanjut
    }

    int hargaPerItem = getHargaMenu(menuPelanggan); // Ambil harga satuan
    int totalHarga = hargaPerItem * jumlah;

    if (namaPelanggan.isEmpty() || menuPelanggan.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Nama pelanggan dan menu harus diisi!");
    } else if (hargaPerItem == 0) {
        JOptionPane.showMessageDialog(this, "Menu tidak dikenali!");
    } else {
        DefaultTableModel model = (DefaultTableModel) tbl_pelanggan.getModel();
        model.addRow(new Object[]{namaPelanggan, menuPelanggan, kategoriMenu, totalHarga, jumlah});

        txt_nama.setText("");
        txt_menu.setText("");
        txt_jumlah.setText(""); // Kosongkan jumlah
        cmb_kategori.setSelectedIndex(0);
        txt_nama.requestFocus();
    }
    }//GEN-LAST:event_smpn2ActionPerformed

