    private void cetakHargaMieAyam() {
        if (harga instanceof HargaMenu) {
            HargaMenu hMenu = (HargaMenu) harga; // Downcasting
            int hargaMie = hMenu.getHarga("mie ayam");
            JOptionPane.showMessageDialog(this, "Harga Mie Ayam adalah Rp " + hargaMie);
        }
    }

 
 private void hitungTotalHarga() {
    DefaultTableModel model = (DefaultTableModel) tbl_pelanggan.getModel();
    int total = 0;

    // Loop melalui semua baris tabel untuk menghitung total harga
    for (int i = 0; i < model.getRowCount(); i++) {
        int harga = (int) model.getValueAt(i, 3); // Mengambil harga dari kolom ke-4 (index 3)
        total += harga; // Menjumlahkan harga
    }

    // Menampilkan total di txt_total
    txt_total.setText(String.valueOf(total));
}
 
